def normalize_ui_language(value: str | None) -> str:
    raw = (value or "en").strip().lower()
    if raw not in SUPPORTED_UI_LANGUAGES:
        return "en"
    return raw


def tr(language: str | None, key: str, **kwargs: Any) -> str:
    lang = normalize_ui_language(language)
    template = _TEXTS.get(lang, _TEXTS["en"]).get(key, _TEXTS["en"].get(key, key))
    if kwargs:
        try:
            return template.format(**kwargs)
        except Exception as exc:
            log.warning("i18n format error for key=%r kwargs=%r: %s", key, kwargs, exc)
            return template
    return template
